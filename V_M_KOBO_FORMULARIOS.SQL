CREATE VIEW V_M_KOBO_FORMULARIOS(
  ID_M_KOBO_FORMULARIOS,
  ID_M_USUARIOS,
  FECHA,
  FECHA_REGISTRO,
  ID,
  ESTATUS,
  ID_M_FORMULARIOS,
  FECHA_FORMULARIO,
  UID,
  FUID,
  NOMBRE_FORMULARIO,
  GRUPO,
  NOMBRE_ESTATUS,
  NOMBRE_USUARIO,
  FECHA_ESTADISTICA,
  REGION,
  DEPARTAMENTO,
  MUNICIPIO,
  CODIGO_ALERTA)
AS
SELECT M_KOBO_FORMULARIOS.ID_M_KOBO_FORMULARIOS AS ID_M_KOBO_FORMULARIOS,
       M_KOBO_FORMULARIOS.ID_M_USUARIOS AS ID_M_USUARIOS,
       M_KOBO_FORMULARIOS.FECHA AS FECHA,
       M_KOBO_FORMULARIOS.FECHA_REGISTRO AS FECHA_REGISTRO,
       M_KOBO_FORMULARIOS.ID AS ID,
       M_KOBO_FORMULARIOS.ESTATUS AS ESTATUS,
       M_KOBO_FORMULARIOS.ID_M_FORMULARIOS AS ID_M_FORMULARIOS,
       M_KOBO_FORMULARIOS.FECHA_FORMULARIO AS FECHA_FORMULARIO,
       M_KOBO_FORMULARIOS.UID AS UID,
       UPPER(M_KOBO_FORMULARIOS.FUID) AS FUID,
       M_FORMULARIOS.NOMBRES AS NOMBRE_FORMULARIO,
       M_FORMULARIOS.GRUPO as GRUPO,
       M_TERMINOS.NOMBRES AS NOMBRE_ESTATUS,
       M_USUARIOS.NOMBRE_COMPLETO AS NOMBRE_USUARIO,
       M_KOBO_FORMULARIOS.FECHA_ESTADISTICA AS FECHA_ESTADISTICA,
       M_KOBO_FORMULARIOS.REGION AS REGION,
       M_KOBO_FORMULARIOS.DEPARTAMENTO AS DEPARTAMENTO,
       M_KOBO_FORMULARIOS.MUNICIPIO AS MUNICIPIO,
       M_KOBO_FORMULARIOS.XCODIGO_ALERTA AS CODIGO_ALERTA

FROM M_KOBO_FORMULARIOS
LEFT JOIN M_TERMINOS    ON (M_TERMINOS.CODIGO1      = M_KOBO_FORMULARIOS.ESTATUS)
LEFT JOIN M_USUARIOS    ON (M_USUARIOS.ID_M_USUARIO = M_KOBO_FORMULARIOS.ID_M_USUARIOS)
LEFT JOIN M_FORMULARIOS ON (M_FORMULARIOS.ID_M_FORMULARIOS = M_KOBO_FORMULARIOS.ID_M_FORMULARIOS)

WHERE
M_KOBO_FORMULARIOS.ID_M_FORMULARIOS != 1121
;


