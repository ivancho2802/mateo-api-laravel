SELECT
D_CONTACTOS.ID_D_contactos AS ID_D_contactos,
D_CONTACTOS.IDX AS IDX,
D_CONTACTOS.TABLA AS TABLA ,
COALESCE (D_CONTACTOS.NOMBRES,'')||' '||COALESCE (D_CONTACTOS.APELLIDOS,'') AS NOMBRES,
D_CONTACTOS.CODIGO1 AS CODIGO1,
D_CONTACTOS.CARGO AS CARGO,
D_CONTACTOS.FECHA AS FECHA,
D_CONTACTOS.PARENTESCO AS PARENTESCO,
D_CONTACTOS.TELEFONO AS TELEFONO,
D_CONTACTOS.TELEFONO_FIJO AS TELEFONO_FIJO,
D_CONTACTOS.TIPO AS TIPO,
D_CONTACTOS.ID_M_USUARIOS AS ID_M_USUARIOS,
D_CONTACTOS.EMPRESA AS EMPRESA,
D_CONTACTOS.CAMPO1 as CAMPO1,
D_CONTACTOS.CAMPO2 as CAMPO2,
D_CONTACTOS.CAMPO3 as CAMPO3,
D_CONTACTOS.CAMPO4 as CAMPO4,
D_CONTACTOS.CAMPO5 as CAMPO5,
COALESCE (D_CONTACTOS.NOMBRES,'')||' - '||COALESCE (D_CONTACTOS.TIPO,'') AS XNOMBRES,

M_USUARIOS.NOMBRE_COMPLETO AS NOMBRE_USUARIO,
D_CONTACTOS.UNICO,
D_CONTACTOS.ID_M_USUARIO AS ID_M_USUARIO,
D_CONTACTOS.CORREO1 AS CORREO1,
D_CONTACTOS.TELEFONO1 AS TELEFONO1,
D_CONTACTOS.TELEFONO2 AS TELEFONO2,
D_CONTACTOS.TELEFONO3 AS TELEFONO3,
D_CONTACTOS.TELEFONO4 AS TELEFONO4,
D_CONTACTOS.TELEFONO5 AS TELEFONO5,
M_FORMULARIOS.NOMBRES AS M_FORMULARIOS_NOMBRES

FROM D_CONTACTOS, M_FORMULARIOS
  --INNER JOIN M_TABLAS  ON M_TABLAS.TABLA = D_CONTACTOS.TABLA
  LEFT JOIN M_USUARIOS ON M_USUARIOS.ID_M_USUARIO = D_CONTACTOS.ID_M_USUARIOS
  LEFT JOIN M_FORMULARIOS ON M_FORMULARIOS.ID_M_FORMULARIOS = D_CONTACTOS.IDX