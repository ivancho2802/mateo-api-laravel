SELECT M_KOBO_RESPUESTAS.ID_M_KOBO_RESPUESTAS AS ID_M_KOBO_RESPUESTAS,
       M_KOBO_RESPUESTAS.ID_M_USUARIOS AS ID_M_USUARIOS,
       M_KOBO_RESPUESTAS.FECHA AS FECHA,
       M_KOBO_RESPUESTAS.FECHA_REGISTRO AS FECHA_REGISTRO,
       M_KOBO_RESPUESTAS.ID AS ID,
       M_KOBO_RESPUESTAS.ESTATUS AS ESTATUS,
       M_KOBO_RESPUESTAS.ID_M_KOBO_FORMULARIOS AS ID_M_KOBO_FORMULARIOS,
       M_KOBO_RESPUESTAS.ID_M_FORMULARIOS AS ID_M_FORMULARIOS,
       M_KOBO_RESPUESTAS.ID_P_FORMULARIOS AS ID_P_FORMULARIOS,
       M_KOBO_RESPUESTAS.TIPO AS TIPO,
       M_KOBO_RESPUESTAS.ROTULO AS ROTULO, 
       M_KOBO_RESPUESTAS.VALOR_C AS VALOR_C, 
       M_KOBO_RESPUESTAS.VALOR_N AS VALOR_N,
       M_KOBO_RESPUESTAS.VALOR AS VALOR, 
       M_KOBO_RESPUESTAS.REFERENCIA AS REFERENCIA,
       M_KOBO_RESPUESTAS.UID AS UID,
       M_KOBO_RESPUESTAS.FUID AS FUID,
       M_KOBO_RESPUESTAS.ID_TMP_RESPUESTAS AS ID_TMP_RESPUESTAS,
       M_KOBO_RESPUESTAS.XROTULO AS XROTULO, 
       M_KOBO_RESPUESTAS.XVALOR AS XVALOR, 
       COALESCE(P_FORMULARIOS.ROTULO, '') AS ROTULO_PLANTILLA, 
       UPPER(P_FORMULARIOS.ROTULO)  AS XROTULO_PLANTILLA, 
       M_TERMINOS.NOMBRES AS NOMBRE_ESTATUS,
       M_USUARIOS.NOMBRE_COMPLETO AS NOMBRE_USUARIO,
       COALESCE(P_FORMULARIOS.XGRUPO, '') AS XGRUPO,
       COALESCE(P_FORMULARIOS.POSICION, '') as POSICION,
       COALESCE(P_FORMULARIOS.POSICION_GRUPO, '') as POSICION_GRUPO,
       M_KOBO_FORMULARIOS.MUNICIPIO AS MUNICIPIO,
       M_KOBO_FORMULARIOS.DEPARTAMENTO AS DEPARTAMENTO,
       M_KOBO_FORMULARIOS.REGION AS REGION,
       M_FORMULARIOS.NOMBRES AS NOMBRE_FORMULARIO,
       M_KOBO_FORMULARIOS.XCODIGO_ALERTA AS XCODIGO_ALERTA,
       M_KOBO_FORMULARIOS.FECHA_ESTADISTICA AS FECHA_ESTADISTICA,
       M_KOBO_FORMULARIOS.FECHA_FORMULARIO AS FECHA_FORMULARIO

FROM M_KOBO_RESPUESTAS
LEFT JOIN M_TERMINOS         ON (M_TERMINOS.CODIGO1      = M_KOBO_RESPUESTAS.ESTATUS)
LEFT JOIN M_USUARIOS         ON (M_USUARIOS.ID_M_USUARIO = M_KOBO_RESPUESTAS.ID_M_USUARIOS)
LEFT JOIN M_KOBO_FORMULARIOS ON (M_KOBO_FORMULARIOS.ID_M_KOBO_FORMULARIOS = M_KOBO_RESPUESTAS.ID_M_KOBO_FORMULARIOS)
LEFT JOIN M_FORMULARIOS      ON (M_FORMULARIOS.ID_M_FORMULARIOS = M_KOBO_FORMULARIOS.ID_M_FORMULARIOS)
INNER JOIN P_FORMULARIOS      ON (P_FORMULARIOS.ID_P_FORMULARIOS = M_KOBO_RESPUESTAS.ID_P_FORMULARIOS)
WHERE P_FORMULARIOS.ESTATUS = 'ACT'