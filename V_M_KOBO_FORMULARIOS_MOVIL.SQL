       M_KOBO_FORMULARIOS.MUNICIPIO AS MUNICIPIO,
       M_KOBO_FORMULARIOS.XCODIGO_ALERTA AS CODIGO_ALERTA

FROM M_KOBO_FORMULARIOS
LEFT JOIN M_TERMINOS    ON (M_TERMINOS.CODIGO1      = M_KOBO_FORMULARIOS.ESTATUS)
LEFT JOIN M_USUARIOS    ON (M_USUARIOS.ID_M_USUARIO = M_KOBO_FORMULARIOS.ID_M_USUARIOS)
LEFT JOIN M_FORMULARIOS ON (M_FORMULARIOS.ID_M_FORMULARIOS = M_KOBO_FORMULARIOS.ID_M_FORMULARIOS)
WHERE M_KOBO_FORMULARIOS.ESTATUS IN ('PENDIENTE','VERIFICADO')


